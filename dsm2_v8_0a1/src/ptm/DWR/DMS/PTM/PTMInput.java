package DWR.DMS.PTM;
import java.io.*;
/**
 *  CLASS
 *  PTMInput
 * 
 *  This class is responsible for input from the PTM model.<br>
 *  The types of input needed maybe<br>
 *  1. In ascii form for a certain # of particles @ certain time intervals<br>
 *  2. In binary form for a certain # of particles @ certain time intervals<br>
 *  3. In information enough to generate post processing questions such
 *  as residence time, pass node information, circling particles etceta.<br>
 *  <p>
 */
public class PTMInput{
  /**
   * constructor
   *  initializes appropriate input streams
   */
public PTMInput(String filename, int type) throws FileNotFoundException{
  inputFilename = filename;
  if (type == Globals.ASCII) {
    inputReader = new BufferedReader(new FileReader(inputFilename));
  }
  else if (type == Globals.BINARY){
    inputStream = new DataInputStream(new FileInputStream(inputFilename));
  }
}
  /*
   * returns type of input
   */
public int getInputType(){
  if(inputReader == null) 
    return Globals.BINARY;
  else 
    return Globals.ASCII;
}
  /**
   *  filename for input
   */
protected String inputFilename;
  /**
   *  buffered stream reader. ASCII format
   */
protected BufferedReader inputReader;
  /**
   *  data input stream. BINARY format
   */
protected DataInputStream inputStream;
}

